from app.utils.text_ru import plural_ru


def test_plural_ru_ads():
    assert plural_ru(1, "объявление", "объявления", "объявлений") == "объявление"
    assert plural_ru(2, "объявление", "объявления", "объявлений") == "объявления"
    assert plural_ru(4, "объявление", "объявления", "объявлений") == "объявления"
    assert plural_ru(5, "объявление", "объявления", "объявлений") == "объявлений"
    assert plural_ru(21, "объявление", "объявления", "объявлений") == "объявление"
    assert plural_ru(11, "объявление", "объявления", "объявлений") == "объявлений"
